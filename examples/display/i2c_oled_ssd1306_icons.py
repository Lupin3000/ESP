def load_image(filename: str):
    """
    load pbm file, convert and return as FrameBuffer class
    :param filename:
    :return: class
    """
    with open(filename, 'rb') as f:
        f.readline()
        f.readline()
        f.readline()
        data = bytearray(f.read())

    return FrameBuffer(data, 25, 25, MONO_HLSB)


if __name__ == '__main__':
    i2c = SoftI2C(sda=Pin(DISPLAY_SDA_PIN), scl=Pin(DISPLAY_SCL_PIN))
    oled = SSD1306_I2C(DISPLAY_WIDTH, DISPLAY_HEIGHT, i2c)

    while True:
        print('[INFO] screen one')
        oled.fill(0)
        start_x = 0
        start_y = 0
        for y, row in enumerate(ICON_A):
            for x, c in enumerate(row):
                oled.pixel(x + start_x, y + start_y, c)
        oled.text('Screen 1', 35, 35)
        oled.show()

        sleep(SCREEN_DURATION)

        print('[INFO] screen two')
        oled.fill(0)
        img_pbm = load_image(ICON_B)
        oled.blit(img_pbm, 0, 0)
        oled.text('Screen 2', 35, 35)
        oled.show()

        sleep(SCREEN_DURATION)
